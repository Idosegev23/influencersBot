// Matching Logic
// ============================================

/**
 * Get a smart thinking message based on the user's message content.
 * Pure keyword matching — no AI, instant.
 */
export function getSmartThinkingMessage(userMessage: string): string {
  const lowerMessage = userMessage.toLowerCase();

  // Find all matching categories and score them
  let bestCategory: ThinkingCategory | null = null;
  let bestScore = 0;

  for (const category of CATEGORIES) {
    let score = 0;
    for (const keyword of category.keywords) {
      if (lowerMessage.includes(keyword.toLowerCase())) {
        score++;
      }
    }
    if (score > bestScore) {
      bestScore = score;
      bestCategory = category;
    }
  }

  // Pick a random message from the best-matching category (or fallback)
  const pool = bestCategory && bestScore > 0 ? bestCategory.messages : DEFAULT_MESSAGES;
  return pool[Math.floor(Math.random() * pool.length)];
}
